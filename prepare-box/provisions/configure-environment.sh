#! /bin/sh -

set -eu

# $1 - Text to be added.
# $2 - File to be modified.
set_once() {
    if grep "$2" "$1" >/dev/null 2>&1
    then
        printf '%s\n' "$1" >> "$2"
    fi
}

set_once DEVELOPER=YES /etc/make.conf

